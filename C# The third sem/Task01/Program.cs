/* Задайте массив из 10 элементов, заполненный числами из
промежутка [-10, 10]. Замените отрицательные элементы на
положительные, а положительные на отрицательные.
Пример
[1 -5 6]
=> [-1 5 -6] */

int[] array = new int[10]; // Создаем массив из 10 элементов
Random random = new Random(); // Создаем экземпляр класса Random для генерации случайных чисел

// Заполняем массив случайными числами от -10 до 10
for (int i = 0; i < array.Length; i++)
{
    array[i] = random.Next(-10, 11); // Генерируем случайное число от -10 до 10
}

// Выводим исходный массив
Console.WriteLine("Исходный массив:");
for (int i = 0; i < array.Length; i++)
{
    Console.Write(array[i] + " ");
}
Console.WriteLine(); // Переход на новую строку для читаемости

// Меняем знаки чисел в массиве
for (int i = 0; i < array.Length; i++)
{
    if (array[i] < 0)
    {
        array[i] = -array[i]; // Если число отрицательное, меняем знак на положительный
    }
    else if (array[i] > 0)
    {
        array[i] = -array[i]; // Если число положительное, меняем знак на отрицательный
    }
}

// Выводим измененный массив
Console.WriteLine("Измененный массив:");
for (int i = 0; i < array.Length; i++)
{
    Console.Write(array[i] + " ");
}
Console.WriteLine(); // Переход на новую строку для читаемости
